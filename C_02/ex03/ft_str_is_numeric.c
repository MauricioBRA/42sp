int	ft_str_is_numeric(char *str)
{
	int	nuor;

	nuor = 0;
	while (str[nuor] != '\0')
	{
		if ((str[nuor] < 48 || str[nuor] > 57))
		{
			return (0);
		}
		nuor++;
	}
	return (1);
}
