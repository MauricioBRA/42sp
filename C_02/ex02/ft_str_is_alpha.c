int	ft_str_is_alpha(char *str)
{
	int	dy;

	dy = 0;
	while (str[dy] != '\0')
	{
		if ((str[dy] < 65 || str[dy] > 90) && (str[dy] < 97 || str[dy] > 122))
		{
			return (0);
		}
		dy++;
	}
	return (1);
}
