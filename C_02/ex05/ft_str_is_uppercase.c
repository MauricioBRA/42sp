int	ft_str_is_uppercase(char *str)
{
	int	tor;

	tor = 0;
	while (str[tor] != '\0')
	{
		if (str[tor] < 65 || str[tor] > 90)
		{
			return (0);
		}
		tor++;
	}
	return (1);
}
