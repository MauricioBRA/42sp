int	ft_str_is_printable(char *str)
{
	int	kar;

	kar = 0;
	while (str[kar] != '\0')
	{
		if (!(str[kar] >= 32 && str[kar] <= 126))
		{
			return (0);
		}
		kar++;
	}
	return (1);
}
