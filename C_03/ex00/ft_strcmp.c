int	ft_strcmp(char *s1, char *s2)
{
	int	mk1;
	int	mk2;

	mk1 = 0;
	mk2 = 0;
	while (s1[mk1] != '\0' && s2[mk2] != '\0')
	{
		if (s1[mk1] != s2[mk2])
		{
			return (s1[mk1] - s2[mk2]);
		}
		mk1++;
		mk2++;
	}
	return (s1[mk1] - s2[mk2]);
}
